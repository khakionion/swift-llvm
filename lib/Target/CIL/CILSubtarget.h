#ifndef LLVM_LIB_TARGET_CIL_CILSUBTARGET_H
#define LLVM_LIB_TARGET_CIL_CILSUBTARGET_H

#include "CILTargetLowering.h"
#include "CILFrameLowering.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "CILGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class CILSubtarget : public CILGenSubtargetInfo {
  CILFrameLowering FrameLowering;  
  CILTargetLowering TargetLowering;
  virtual void anchor();

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  CILSubtarget(const Triple &TT, const std::string &CPU,
                 const std::string &FS, const TargetMachine &TM);
  const CILFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified 
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

};
} // End llvm namespace

#endif
